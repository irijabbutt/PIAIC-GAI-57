// 3. Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
let nam_e = "john doe";
console.log("Lowercase: " + nam_e.toLowerCase());
console.log("Uppercase: " + nam_e.toUpperCase());
console.log("Titlecase: " + nam_e.charAt(0).toUpperCase() + nam_e.slice(1).toLowerCase());
